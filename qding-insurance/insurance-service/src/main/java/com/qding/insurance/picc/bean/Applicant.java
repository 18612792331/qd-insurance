package com.qding.insurance.picc.bean;

/**
 * 投保请求报文中的Applicant节点
 * @author zhangxiaojun
 *
 */
public class Applicant {
	//投保人姓名
	private String appliName;
	//投保人证件类型
	private String appliIdType;
	//投保人证件号
	private String appliIdNo;
	//投保人手机号
	private String appliIdMobile;
	//投保人邮箱
	private String appliIdEmail;
	//投保人与被保险人的关系
	private String appliIdentity;
	public String getAppliName() {
		return appliName;
	}
	public void setAppliName(String appliName) {
		this.appliName = appliName;
	}
	public String getAppliIdType() {
		return appliIdType;
	}
	public void setAppliIdType(String appliIdType) {
		this.appliIdType = appliIdType;
	}
	public String getAppliIdNo() {
		return appliIdNo;
	}
	public void setAppliIdNo(String appliIdNo) {
		this.appliIdNo = appliIdNo;
	}
	public String getAppliIdMobile() {
		return appliIdMobile;
	}
	public void setAppliIdMobile(String appliIdMobile) {
		this.appliIdMobile = appliIdMobile;
	}
	public String getAppliIdEmail() {
		return appliIdEmail;
	}
	public void setAppliIdEmail(String appliIdEmail) {
		this.appliIdEmail = appliIdEmail;
	}
	public String getAppliIdentity() {
		return appliIdentity;
	}
	public void setAppliIdentity(String appliIdentity) {
		this.appliIdentity = appliIdentity;
	}
	
	
	
}
